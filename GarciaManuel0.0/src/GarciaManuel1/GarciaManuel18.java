package GarciaManuel1;

public class GarciaManuel18 
{

	public static void main(String[] args) 
	{
		// Condicional
		/* 
		 * if {codigo si es cierto} else {codigo si es falso}
		 */

		int num = 3;
		
		switch (num) 
		{
			case 0:
			
			System.out.println ("El numero es 0.");
			
				break;
			
			case 1:
			
			System.out.println ("El numero es 1.");
			
				break;
			
			case 2:
				
				System.out.println ("El numero es 2.");
				
				break;
				
			case 3:
				
				System.out.println ("El numero es 3.");
				
				break;
				
			case 4:
				
				System.out.println ("El numero es 4.");
				
				break;
			
		
		}
		
		
		
		// Switch: Estructura de control condicional, comprobación múltiple

		
		
		
	}

}

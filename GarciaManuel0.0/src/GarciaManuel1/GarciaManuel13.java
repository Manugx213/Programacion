package GarciaManuel1;

public class GarciaManuel13 
{

	public static void main(String[] args) 
	{
		// SUMA: +
		// RESTA: -
		// MULTIPLICACION: *
		// DIVISION: /
		// RESTO: %

		double operandoUno;
		operandoUno= 15.248;
		double operandoDos = 7.95;
		double resultado = 0;
		
		// operaciones con variables
		
		
		//SUMA
		
		resultado = operandoUno + operandoDos;
		System.out.println(resultado);
		
		
		//RESTA
		
		resultado = operandoUno - operandoDos;
		System.out.println(resultado);
		
		//MULTIPLICACION
		
		resultado = operandoUno * operandoDos;
		System.out.println(resultado);
		
		
		//DIVISION
		resultado = operandoUno / operandoDos;
		System.out.println(resultado);
		
		//RESTO
		
		resultado = operandoUno % operandoDos;
		System.out.print(resultado);
		
		

		
		
		
		
	}

}
